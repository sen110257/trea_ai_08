export const drawLotteryStick = () => {
  const weights = [5, 15, 25, 30, 10, 5, 4, 3, 2, 1]
  const totalWeight = weights.reduce((a, b) => a + b, 0)
  let random = Math.random() * totalWeight
  
  for (let i = 0; i < lotterySticks.length; i++) {
    random -= weights[i]
    if (random <= 0) {
      return lotterySticks[i]
    }
  }
  
  return lotterySticks[0]
}

